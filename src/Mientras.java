
//UTILIZANDO MIENTRAS - WHILE 
public class Mientras {
    public static void main(String[] args) {
        int c = 0;
        
        while(c <= 10){ //miestras que c sea menor o igual que 10 que se ejecute el bloque de codigo, si llega a ser mayor, que se detenga
            System.out.println("El valor de c es: "+ c);
            c++; //operador de incremento, para que C se increment uno en uno por cada iteracion del while
            
            
        }
        
    }
}
